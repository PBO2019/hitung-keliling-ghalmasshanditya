public class BujurSangkar {
    private int sisi;
    private int keliling;

    public void hitungkelilingbujursangkar() {
        this.keliling = 4 * sisi;
    }

    public void setSisi(int sisi){
        this.sisi=sisi;
    }

    public int getKelilingBujursangkar(){
        return keliling;
    }
}
